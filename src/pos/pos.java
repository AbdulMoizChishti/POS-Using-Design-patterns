    @SuppressWarnings("empty-statement")
    public void salesproduct() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Table is Empty");
        } else {
            String pcode, pname, qty, price, amount;
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                conn = (java.sql.Connection) DriverManager.getConnection("jdbc:mysql://localhost/pos", "root", "");
//                conn.setAutoCommit(false);

                for (int i = 0; i < model.getRowCount(); i++) {
                    pcode = model.getValueAt(i, 0).toString();
                    pname = model.getValueAt(i, 1).toString();
                    qty = model.getValueAt(i, 2).toString();
                    price = model.getValueAt(i, 3).toString();
                    amount = model.getValueAt(i, 4).toString();

                    String query1 = "INSERT INTO salesproduct(Barcode,Name,Qty,Price,Amount)values(?,?,?,?,?)";
                    pst1 = conn.prepareStatement(query1);

                    pst1.setString(1, pcode);
                    pst1.setString(2, pname);
                    pst1.setString(3, qty);
                    pst1.setString(4, price);
                    pst1.setString(5, amount);
                    pst1.execute();

                }

                JOptionPane.showMessageDialog(this, "Data Added");
                model.setRowCount(0);

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

