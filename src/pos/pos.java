    private void barcode_readKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_barcode_readKeyPressed

        //        Barcode reader starts here
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String pcode = barcode_read.getText();

            try {

                Class.forName("com.mysql.cj.jdbc.Driver");
                conn = (java.sql.Connection) DriverManager.getConnection("jdbc:mysql://localhost/pos", "root", "");

                pst = conn.prepareStatement("select * from products where Barcode = ?");

                pst.setString(1, pcode);
                rs = pst.executeQuery();

                if (rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Product Code Not Found");
                } else {
                    String pname = rs.getString("Name");
                    String price = rs.getString("Price");

                    pn.setText(pname.trim());
                    pri.setText(price.trim());

                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
// Barcode reader ends here


    }//GEN-LAST:event_barcode_readKeyPressed

